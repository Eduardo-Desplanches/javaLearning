package TiposDeDados;

public class DadosPrimitivos {
    public static void main(String[] args) {
        /*Dados primitivos - int, double, float, char, boolean, short
        * */
        int idade = 17; // valor máximo de Int: 2.147.483.647
        double altura = 1.65;
        char iniciar = 'N';
        boolean vivoOuMorto = false;
        Long saldoBancario = 999999L; // Valor máximo: 9 trilhões e alguns quebrados

        System.out.println("saldoBancario é = " + saldoBancario);
        System.out.println("A idade do Naruto é: " + idade);
    }
}
